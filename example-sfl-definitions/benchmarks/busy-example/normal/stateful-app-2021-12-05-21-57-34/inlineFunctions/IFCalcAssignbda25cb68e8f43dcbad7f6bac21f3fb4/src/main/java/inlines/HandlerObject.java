package inlines;

public class HandlerObject {

    public HandlerObject() {
    }

    private long x;

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    private double d;

    public double getD() {
        return d;
    }

    public void setD(double d) {
        this.d = d;
    }

    private String s;

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}
